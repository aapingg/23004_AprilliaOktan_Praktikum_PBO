    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        // TODO add your handling code here:
        try {
            int tahun = Integer.parseInt(TahunText.getText());
            String bulan = (String) jComboBox1.getSelectedItem();

            HitungHari hitungHari = new HitungHari();
            int jumlahHari = hitungHari.hitung(tahun, bulan);
            JumlahHari.setText("Jumlah Hari Adalah: " + jumlahHari);
        }
        
        catch (NumberFormatException e) {
            JumlahHari.setText("Tahun tidak valid!");
        }
    }//GEN-LAST:event_HitungActionPerformed

