    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
        try {
            String tahun = TahunText.getText();
            String bulan = (String) jComboBox1.getSelectedItem();
            String jumlahHariText = JumlahHari.getText();

            String data = "Tahun: " + tahun + ", Bulan: " + bulan + ", " + jumlahHariText;
            
            java.nio.file.Files.write(java.nio.file.Paths.get("hasil_perhitungan.txt"), 
                                       data.getBytes(), 
                                       java.nio.file.StandardOpenOption.CREATE, 
                                       java.nio.file.StandardOpenOption.APPEND);

            JumlahHari.setText("Data berhasil disimpan!");
        } 
        
        catch (IOException e) {
            JumlahHari.setText("Gagal menyimpan data!");
        }
    }//GEN-LAST:event_SimpanActionPerformed

